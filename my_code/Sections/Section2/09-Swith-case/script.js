const day = 'monday'; 

switch(day) {
    case 'monday': //day === 'monday
        console.log ('Monday');
        break;
    case 'tuesday': 
        console.log ('Tuesday');
        break;
    case 'wednesday': 
        console.log ('Wednesday');
        break;
    case 'sunday': 
        console.log ('Sunday');
        break;
    default:
        console.log ('Not a valid day!');
}